#!/usr/bin/env python3
"""Post-process a `bal openapi` generated client so it uses the
ballerinax/sap.businessone session-auth wrapper instead of a plain http:Client,
and carries the Apache license header.

Usage: sanitize_connector.py <connector-dir>

Only touches the generated .bal files (client.bal, types.bal, utils.bal). The
package Ballerina.toml/README are generated by the Gradle build. Idempotent:
safe to re-run after regeneration.
"""
